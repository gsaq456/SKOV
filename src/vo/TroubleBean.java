package vo;

public class TroubleBean {
	
	private int product_num;
	private String product_id;
	private String product_type;
	private String product_info;
	private String product_pr;
	private String trouble_picture;
	private double starpoint;
	
	public double getStarpoint() {
		return starpoint;
	}
	public void setStarpoint(double starpoint) {
		this.starpoint = starpoint;
	}
	public int getProduct_num() {
		return product_num;
	}
	public void setProduct_num(int product_num) {
		this.product_num = product_num;
	}
	public String getProduct_id() {
		return product_id;
	}
	public void setProduct_id(String product_id) {
		this.product_id = product_id;
	}
	public String getProduct_type() {
		return product_type;
	}
	public void setProduct_type(String product_type) {
		this.product_type = product_type;
	}
	public String getProduct_info() {
		return product_info;
	}
	public void setProduct_info(String product_info) {
		this.product_info = product_info;
	}
	public String getProduct_pr() {
		return product_pr;
	}
	public void setProduct_pr(String product_pr) {
		this.product_pr = product_pr;
	}
	public String getTrouble_picture() {
		return trouble_picture;
	}
	public void setTrouble_picture(String trouble_picture) {
		this.trouble_picture = trouble_picture;
	}
	
	

}
