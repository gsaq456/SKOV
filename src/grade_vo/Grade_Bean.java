package grade_vo;

import java.sql.Date;

public class Grade_Bean {
	
	private int UP_NUM;
	private String UP_NAME;
	private String UP_PASS;
	private String UP_SUBJECT;
	private String UP_CONTENT;
	private Date UP_DATE;

	
	


	public int getUP_NUM() {
		return UP_NUM;
	}
	
	public void setUP_NUM(int up_num) {
		UP_NUM = up_num;
	}
	
	public String getUP_NAME() {
		return UP_NAME;
	}
	
	public void setUP_NAME(String up_name) {
		UP_NAME = up_name;
	}
	
	public String getUP_PASS() {
		return UP_PASS;
	}
	
	public void setUP_PASS(String up_pass) {
		UP_PASS = up_pass;
	}
	
	public String getUP_SUBJECT() {
		return UP_SUBJECT;
	}
	
	public void setUP_SUBJECT(String up_subject) {
		UP_SUBJECT = up_subject;
	}
	
	public String getUP_CONTENT() {
		return UP_CONTENT;
	}
	
	public void setUP_CONTENT(String up_content) {
		UP_CONTENT = up_content;
		
	}
	
	
	
	public Date getUP_DATE() {
		return UP_DATE;
	}
	
	public void setUP_DATE(Date up_date) {
		UP_DATE = up_date;
	}

	

	
	
}